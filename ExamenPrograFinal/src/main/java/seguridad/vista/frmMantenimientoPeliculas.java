    public void llenadoDeTablas() {
        DefaultTableModel modelo = new DefaultTableModel();
        modelo.addColumn("Nombre Pelicula");
        modelo.addColumn("Clasificaci贸n");
        modelo.addColumn("Genero");
        modelo.addColumn("Subtitulado");
        modelo.addColumn("Idioma");
        modelo.addColumn("precio");
        daoPeliculas vendedorDAO = new daoPeliculas();
        List<clsPeliculas> vendedores = vendedorDAO.select();
        tablaVendedores.setModel(modelo);
        String[] dato = new String[6];
        for (int i = 0; i < vendedores.size(); i++) {
            dato[0] = vendedores.get(i).getNombre();
            dato[1] = vendedores.get(i).getClasificaci贸n();
            dato[2] = vendedores.get(i).getGenero();
            dato[3] = vendedores.get(i).getSubtitulado();
            dato[4] = vendedores.get(i).getIdioma();
            dato[5] = Integer.toString(vendedores.get(i).getidprecio());
            
            //System.out.println("vendedor:" + vendedores);
            modelo.addRow(dato);
        }
    }

